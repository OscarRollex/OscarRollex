//program to determine use of arithmetic operators
#include<stdio.h>
int main()
{
    int x=20, y=4, z;
    z=x+y;
    printf("x+y=%d \n", z);
    z=x-y;
    printf("x-y=%d \n", z);
    z=x*y;
    printf("x*y=%d \n", z);
    z=x/y;
    printf("x/y=%d \n", z);
    z=x%y;
    printf("x%y=%d \n", z);
    return 0;
}
